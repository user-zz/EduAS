public class TempUserbean {
		String tempusername; //用户名
		String temprealname; //真实姓名
		String tempMajor; //专业或院系
		String tempid; //身份
		
		public String getTempusername() {
			return tempusername;
		}
		public void setTempusername(String tempusername) {
			this.tempusername = tempusername;
		}
		public String getTemprealname() {
			return temprealname;
		}
		public void setTemprealname(String temprealname) {
			this.temprealname = temprealname;
		}
		public String getTempMajor() {
			return tempMajor;
		}
		public void setTempMajor(String tempMajor) {
			this.tempMajor = tempMajor;
		}
		public String getTempid() {
			return tempid;
		}
		public void setTempid(String tempid) {
			this.tempid = tempid;
		}
		
}
